package chapterfour;

public class GradeAverageFnx {
    public String numberOfGrade(int i) {
        if (i == 0) {
            return "1st";
        } else if (i == 1) {
            return "2nd";
        } else if (i == 2) {
            return "3rd";
        } else if (i == 3) {
            return "4th";
        } else if (i == 4) {
            return "5th";
        } else if (i == 5) {
            return "6th";
        } else if (i == 6) {
            return "7th";
        } else if (i == 7) {
            return "8th";
        } else if (i == 8) {
            return "9th";
        }else {
            return "10th";
        }
    }
}
